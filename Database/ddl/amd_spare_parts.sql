(
  PART_NO                    VARCHAR2(50 BYTE)  NOT NULL,
  MFGR                       VARCHAR2(13 BYTE),
  DATE_ICP                   DATE,
  DISPOSAL_COST              NUMBER,
  DISPOSAL_COST_DEFAULTED    NUMBER,
  ERC                        VARCHAR2(1 BYTE),
  ICP_IND                    VARCHAR2(3 BYTE),
  NOMENCLATURE               VARCHAR2(512 BYTE),
  ORDER_LEAD_TIME            NUMBER,
  ORDER_LEAD_TIME_DEFAULTED  NUMBER,
  ORDER_UOM                  VARCHAR2(2 BYTE),
  ORDER_UOM_DEFAULTED        VARCHAR2(2 BYTE),
  SCRAP_VALUE                NUMBER,
  SCRAP_VALUE_DEFAULTED      NUMBER,
  SERIAL_FLAG                VARCHAR2(1 BYTE),
  SHELF_LIFE                 NUMBER,
  SHELF_LIFE_DEFAULTED       NUMBER,
  UNIT_COST                  NUMBER,
  UNIT_COST_DEFAULTED        NUMBER,
  UNIT_VOLUME                NUMBER,
  UNIT_VOLUME_DEFAULTED      NUMBER,
