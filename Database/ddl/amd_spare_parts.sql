  TACTICAL                   VARCHAR2(1 BYTE),
  ACTION_CODE                VARCHAR2(1 BYTE),
  LAST_UPDATE_DT             DATE,
  PROCESS_BYPASS             VARCHAR2(40 BYTE),
  ACQUISITION_ADVICE_CODE    VARCHAR2(1 BYTE),
  UNIT_OF_ISSUE              VARCHAR2(2 BYTE)
